        private static List<string> ClearData()
        {
            var data = _map.Split('\n')
                .Select(x => x.Trim())
                .ToList();

            return data;
        }

        public static int Exercise1(int right, int down)
        {
            var data = ClearData();
            int result = 0;

            var positionX = 0;
            var lineLenght = 31;

            for(var i = down; i < data.Count; i = i + down )
            {
                //var aux = (positionX + 3);
                //positionX = aux > lineLenght ? 0 : aux;

                positionX = (positionX + right) % lineLenght;
                var isTree = data[i][positionX] == '#';

                if (isTree) result++;
            }
            
            return result;
        }

        public static long Exercise2()
        {
            long result = 0;

            var res1 = (long)Exercise1(1, 1);
            var res2 = (long)Exercise1(3, 1);
            var res3 = (long)Exercise1(5, 1);
            var res4 = (long)Exercise1(7, 1);
            var res5 = (long)Exercise1(1, 2);

            result = res1 * res2 * res3 * res4 * res5;

            return result;
        }


