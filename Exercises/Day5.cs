        private static List<string> ClearData()
        {
            var list = _data.Split("\r\n")
                .ToList();

            return list;
        }

        private static int GetHalf(string data, int range, char lowCharacter, char upperCharacter)
        {
            var result = 0;

            var enumerate = Enumerable.Range(0, range).ToList();

            foreach (var letter in data)
            {
                if (enumerate.Count() == 1) break;
                var half = enumerate.Count() / 2;

                if (letter == lowCharacter)
                {
                    enumerate = enumerate.Take(half).ToList();
                }
                else if (letter == upperCharacter) 
                {
                    enumerate = enumerate.Skip(half).ToList();
                }
                else throw new ApplicationException($"Wrong letter in get row {letter}");
            }

            result = enumerate.First();

            return result;
        }

        public static List<int> Exercise1()
        {
            var result = 0;

            var data = ClearData();
            var seatList = new List<int>();

            foreach (var bPass in data)
            {
                var rowString = new string(bPass.Take(7).ToArray());
                var seatString = new string(bPass.TakeLast(3).ToArray()); ;

                var rowNumber = GetHalf(rowString, 128, 'F', 'B');
                var seatRowNumber = GetHalf(seatString, 8, 'L', 'R');

                var seatNumber = rowNumber * 8 + seatRowNumber;

                seatList.Add(seatNumber);
            }

            result = seatList.Max();

            return seatList;
        }

        public static int Exercise2()
        {
            var result = 0;

            var seats = Exercise1();

            seats.Sort();

            var min = seats.Min();
            var max = seats.Max();

            var enumerable = Enumerable.Range(min, max - min).ToList();

            var except = enumerable.Except(seats).ToList();            

            return except.First();
        }

